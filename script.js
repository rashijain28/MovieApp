function compareTitle( a, b ) {
  if ( a.title < b.title ){
    return -1;
  }
  if ( a.title > b.title ){
    return 1;
  }
  return 0;
}

function compareDate( a, b ) {
  if ( a.release_date < b.release_date ){
    return -1;
  }
  if ( a.release_date > b.release_date ){
    return 1;
  }
  return 0;
}



